            ';

        $table = '
            <div class="row">

              <div class="col-md-12">
                <div class="form-group">
                  <label>Id: </label>
                  <input type="text" id="txttransid" name="txttransid" class="form-control" value="'.$trans_id.'" style="background:#fff" readonly />
