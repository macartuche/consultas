class Datatable extends CI_Controller {
    function __construct() {
        parent::__construct();
        $this->load->library("dashtemp_template");
    }
    
    public function index() {
		$data = array();
        $this->dashtemp_template->display_dashtemp('page/datatable_vw', $data);
    }

    public function gridview() {
        $where = $this->input->post("where");
        
        $database = "default";
        $column = array('trans_id', 'trans_date', 'trans_type', 'trans_txt', 'trans_status', 'trans_amount');
        $table = "datatable";
        $list = $this->datatables_mdl->get_datatables($database, $table, $column, $where);
        $data = array();
        $no = $_POST['start'];
        foreach ($list as $line) 
        {
            $no++;          
            $row = array();
            $id = md5($line->trans_id);
            $link = '
                <div class="btn-group">
                  <a href="#" data-toggle="dropdown" style="color:#111"><i class="fa fa-folder-open"></i></a>
                  <ul class="dropdown-menu" role="menu">
                    <li><a href="#" style="color:#111" data-toggle="modal" data-target="#modalForm" onclick="generateModalView(\''.$line->trans_id.'\')">View</a></li>
                    <li><a href="#" style="color:#111" onclick="generateModalForm(\'edit\', \''.$line->trans_id.'\')">Update</a></li>
                    <li><a href="#" style="color:#111" onclick="doDelete(\''.$line->trans_id.'\')">Delete</a></li>
                  </ul>
                </div>
            ';
            $row[] = '<div style="text-align:center">'.$link.'</div>';
            $row[] = '<div style="text-align:center">'.$line->trans_id.'</div>';
            $row[] = '<div style="text-align:center">'.date("m/d/Y", strtotime($line->trans_date)).'</div>';
            $row[] = '<div style="text-align:center">'.$line->trans_type.'</div>';
            $row[] = '<div style="text-align:center">'.$line->trans_txt.'</div>';
            $row[] = '<div style="text-align:center">'.$line->trans_status.'</div>';
            $row[] = '<div style="text-align:center">'.$line->trans_amount.'</div>';
            $data[] = $row;
        }
 
        $output = array(
            "draw" => $_POST['draw'],
            "recordsTotal" => $this->datatables_mdl->count_all($database, $table, $column),
            "recordsFiltered" => $this->datatables_mdl->count_filtered($database, $table, $column, $where),
            "data" => $data,
        );
        
        echo json_encode($output);
    }

    public function generateId(){
        $qry = "select max(ifnull(trans_id, '00000')) as total from datatable where year(created_date) = '".date("Y")."'";
        $rs = getByQuery($qry);
        $last = (int)right($rs[0]->total, 5) + 1;
        $id = "TR".right(date("Y"), 2).right("00000".$last, 5);
        return $id;
    }

    public function save()
    {
        $txtstate = $this->input->post("txtstate");
        $txttransid = $this->input->post("txttransid");
        $txttransdate = $this->input->post("txttransdate");
        $ddltranstype = $this->input->post("ddltranstype");
        $txttranstxt = $this->input->post("txttranstxt");
        $ddltransstatus = $this->input->post("ddltransstatus");
        $txttransamount = $this->input->post("txttransamount");

        if($txtstate == 'add')
        {
            $insert = array(
                'trans_id' => $this->generateId(),
                'trans_date' => date("Y-m-d H:i:s", strtotime($txttransdate)),
                'trans_type' => $ddltranstype,
                'trans_txt' => $txttranstxt, 
                'trans_status' => $ddltransstatus,   
                'trans_amount' => $txttransamount,
                'created_date' => date("Y-m-d H:i:s")         
            );
            insertData("datatable", $insert);
            $data['msg'] = "Process successful";
            echo json_encode($data);
        }
        else
        {            
            $update = array(
                'trans_date' => date("Y-m-d H:i:s", strtotime($txttransdate)),
                'trans_type' => $ddltranstype,
                'trans_txt' => $txttranstxt, 
                'trans_status' => $ddltransstatus,   
                'trans_amount' => $txttransamount,
                'updated_date' => date("Y-m-d H:i:s")              
            );
            updateData("datatable", $update, "trans_id = '".$txttransid."'");
            $data['msg'] = "Process successful";
            echo json_encode($data);
        }
    }

    public function form() {
